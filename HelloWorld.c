#include<stdio.h>

int main()
{
	uint64_t m=0b100000000000000000000000000000000;
	uint64_t ary[]={
		0b100010011111001000000100000011111,
		0b100010010000001000000100000010001,
		0b100010010000001000000100000010001,
		0b111110011110001000000100000010001,
		0b100010010000001000000100000010001,
		0b100010010000001000000100000010001,
		0b100010011111001111100111110011111,
		0b000000000000000000000000000000000,
		0b100010011111001111100100000011110,
		0b100010010001001000100100000010001,
		0b100010010001001111100100000010001,
		0b101010010001001100000100000010001,
		0b110110010001001010000100000010001,
		0b100010010001001001000100000010001,
		0b100010011111001000100111110011110};
	for (int i=0; i<15; i++)
	{
		uint64_t n=m;
		for (int j=0; j<33; j++)
		{
			if (ary[i] & n)
				printf("%1c",'C');
			else
				printf("%1c",' ');
			n>>=1;
		}
		printf("\n");
	}
	return 0;
}
